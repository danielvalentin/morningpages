	<h2>The Minimalist Journal</h2>
	<p>
		Morning Pages is a form of writing therapy based on the self-help book The Artist's Way: A Spiritual Path To Higher Creativity by <a title="Julia Cameron" href="http://en.wikipedia.org/wiki/Julia_Cameron">Julia Cameron</a>. The idea is simple. Write three pages a day. Write them about anything you want. All that matters is that you write.
	</p>
	
	<h2>Why we're different</h2>
	<p>
		This isn't a website focused around making friends, building social acceptance, or sharing your ideas. It's not blogging, micro-blogging, or anything of the sort. There are plenty of social networks that do that. Morning Pages is here for you to spontaneously express your thoughts into some sort of semi-organized fashion. We say semi-organized because Morning Pages is not about organized thought and editing. It's about writing. We're here to give you a platform for you to write. We did the work. All you have to do is write. Oh yeah, did we mention we're free?
	</p>
